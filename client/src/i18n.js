// Number formatting (Arabic-Indic optional, default Western digits since OMR uses 3 decimals)
export const fmt = (n, lang) => {
  if (n === null || n === undefined) return '—';
  const num = Number(n);
  if (Number.isNaN(num)) return '—';
  return num.toLocaleString(lang === 'ar' ? 'ar-OM' : 'en-OM', { minimumFractionDigits: 0, maximumFractionDigits: 3 });
};
export const money = (n, lang, sym = 'ر.ع') => {
  return `${fmt(Number(n).toFixed(3), lang)} ${lang === 'ar' ? 'ر.ع' : 'OMR'}`;
};
export const dt = (s, lang) => {
  if (!s) return '—';
  const d = new Date(s);
  return d.toLocaleString(lang === 'ar' ? 'ar-OM' : 'en-OM', { dateStyle: 'medium', timeStyle: 'short' });
};
export const dateOnly = (s, lang) => {
  if (!s) return '—';
  const d = new Date(s);
  return d.toLocaleDateString(lang === 'ar' ? 'ar-OM' : 'en-OM', { dateStyle: 'medium' });
};
